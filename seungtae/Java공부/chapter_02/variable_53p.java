package chapter_02;

public class variable_53p {

	public static void main(String[] args) {
		// 문자형
		
		char ch1 = 'A'; // char형 변수 선언 및 값 입력 
		System.out.println(ch1); // ch1변수안의 값 출력 
		System.out.println((int)ch1); // 인코딩 
		
		char ch2 = 66; // char형 변수 선언 및 값 입력 
		System.out.println(ch2); // 디코딩 
		
		int ch3 = 67; // 인트형 변수 선언 및 값 입
		System.out.println(ch3); // ch3변수안의 값 출
		System.out.println((char)ch3); // ch3데이터값을 char로 감싸 형변환 후 값 출
		
		// 1분 복습 
		char ch4 = 'Z';
		System.out.println(ch4);
		System.out.println((int)ch4);
		
		char ch5 = 38;
		System.out.println(ch5);
		System.out.println((char)38);
		
		char ch6 = 97;
		System.out.println(ch6);
		System.out.println((int)97);
	}

}
