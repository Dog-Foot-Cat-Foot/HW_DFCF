package chapter_02;

public class variable_55p {

	public static void main(String[] args) {
		// 문자형 연습
		
		int a = 65;
		int b = -66;
		
		char a2 = 65;
		// char b2 = -66; 문자형 변수에 음수를 넣으면 오류 발생 
		
		System.out.println((char)a); // 정수65에 해당하는 문자형 출력 
		System.out.println((char)b); 
		System.out.println(a2); // 정수65에 해당하는 문자형 출

	}

}
