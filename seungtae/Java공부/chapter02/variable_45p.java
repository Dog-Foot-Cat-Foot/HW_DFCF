package chapter_02;

public class variable_45p {

	public static void main(String[] args) {
		// 변수선언 및 사용 

		int level;	// 정수형 변수 level 선언
		level=10;	// level 변수에 데이터 10 대
		
		System.out.println(level); // level 데이터 값 출력   
		
		//1분 복습
		int age = 30; // 정수형 변수 age 선어과 동시에 데이터값 30입
		
		System.out.println(age); // age 데이터 값 출력 
	}
}