package chapter_02;

public class variable_57p {

	public static void main(String[] args) {
		// 실수형 연습
		
		double dnum = 3.14;
		float fnum = 3.14F; // F는 float형 값이 대입된다는 식별자(=알려주는애)
		
		System.out.println(dnum);
		System.out.println(fnum); // 출력할 땐 식별자는 나오지 않음 

		// 자바에서 실수는 double형을 기본으로 한다. 
		// float(4바이트)보다 double(8바이트) 더 정밀하다.
	}

}
