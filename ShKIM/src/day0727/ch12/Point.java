package day0727.ch12;

//------------------------------------------
// T, V 2개의 자료형 매개변수를 받는 Point 클래스 선언
//------------------------------------------
public class Point<T, V> {

	//------------------------------------------
	// <1> T 자료형 멤버 변수 x 선언
	// <2> V 자료형 멤버 변수 y 선언
	//------------------------------------------
	T x;			// <1>
	V y;			// <2>

	//------------------------------------------
	// x 와 y 값을 지정하는 생성자 선언
	//------------------------------------------
	Point(T x, V y) {
		this.x = x;
		this.y = y;
	}
	
	//------------------------------------------
	// T 자료형 x 데이터를 리턴하는 제네릭 메서드 선언
	//------------------------------------------
	public T getX() {
		return x;
	}
	
	//------------------------------------------
	// V 자료형 y 데이터를 리턴하는 제네릭 메서드 선언
	//------------------------------------------
	public V getY() {
		return y;
	}
	
}
