// package란 소스 코드의 묶음을 나타냄
package day0709.ch02;

// Variable1 이라는 이름으로 클래스 생성
public class Ex01_Variable1 {
	
	// main()은 자바 프로그램의 첫 시작을 나타내는 함수
	public static void main(String[] args) {

		// level 이라는 이름의 변수를 정수 자료형으로 선언
		// 변수를 선언하면 변수에 값을 넣을 수 있고,
		// 변수 이름을 사용하여 변수에 들어 있는 데이러를 가져올 수 있다.
		int level;
		
		// 선언한 level 변수에 데이터 10을 대입
		// 변수명 = 데이터
		level = 10;
		
		// level 데이터 출력
		System.out.println(level);

	}

}
